void ABlasterPlayerController::BroadcastElim(APlayerState* Attacker, APlayerState* Victim)
{
	ClientElimAnnouncement(Attacker, Victim);
}

void ABlasterPlayerController::ClientElimAnnouncement_Implementation(APlayerState* Attacker, APlayerState* Victim)
{
	APlayerState* Self = GetPlayerState<APlayerState>();
	if (Attacker && Victim && Self)
	{
		BlasterHUD = BlasterHUD == nullptr ? Cast<ABlasterHUD>(GetHUD()) : BlasterHUD;
		if (BlasterHUD)
		{
			if (Attacker == Self && Victim != Self)
			{
				BlasterHUD->AddElimAnnouncement("You", Victim->GetPlayerName());
				return;
			}
			if (Victim == Self && Attacker != Self)
			{
				BlasterHUD->AddElimAnnouncement(Attacker->GetPlayerName(), "you");
				return;
			}
			if (Attacker == Victim && Attacker == Self)
			{
				BlasterHUD->AddElimAnnouncement("You", "yourself");
				return;
			}
			if (Attacker == Victim && Attacker != Self)
			{
				BlasterHUD->AddElimAnnouncement(Attacker->GetPlayerName(), "themselves");
				return;
			}
			BlasterHUD->AddElimAnnouncement(Attacker->GetPlayerName(), Victim->GetPlayerName());
		}
	}
}

void ABlasterPlayerController::BeginPlay()
{
	Super::BeginPlay();

	BlasterHUD = Cast<ABlasterHUD>(GetHUD());
	if (HasAuthority() && IsLocalController())
	{
		FTimerHandle TimerHandle;
		GetWorldTimerManager().SetTimer(
			TimerHandle,
			this,
			&ABlasterPlayerController::ServerCheckMatchState,
			0.1f,//服务端PlayerController的BeginPlay比GameMode先执行，导致LevelStartingTime还未初始化，解决办法就是等0.1秒再调用
			false
		);
	}
	else
	{
		ServerCheckMatchState();
	}
	//角色死亡重新复活并不会调用ABlasterPlayerController::BeginPlay
	//在即将进入Cooldown的前一秒击杀该角色，会导致进入Cooldown之后
	//角色才重新复活，此时未设置BlasterCharacter->bDisableGameplay = true;
	//会导致复活角色在Cooldown还能进行移动和其他操作
	//解决bug方法：在OnPossess中判断状态是否为Cooldown，如果是，设置BlasterCharacter->bDisableGameplay = true;
}

